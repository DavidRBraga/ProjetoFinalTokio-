package model;

public class Servico {

	private long cd_servico;
	private String nm_servico, ds_servico;

	public long getCd_servico() {
		return cd_servico;
	}

	public void setCd_servico(long cd_servico) {
		this.cd_servico = cd_servico;
	}

	public String getNm_servico() {
		return nm_servico;
	}

	public void setNm_servico(String nm_servico) {
		this.nm_servico = nm_servico;
	}

	public String getDs_servico() {
		return ds_servico;
	}

	public void setDs_servico(String ds_servico) {
		this.ds_servico = ds_servico;
	}

}
